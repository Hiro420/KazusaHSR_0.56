	    lock (_consoleLock)
	    {
		    Colorful.Console.WriteLine(Kazusa, colorCode);
		    Colorful.Console.ResetColor();
	    }
    }

    internal enum LogColors
    {
	    None = 0xFFFFFF,
	    INFO = 0x61AFEF,
	    WARNING = 0xE5C07B,
	    ERROR = 0xE06C75,
	    SUCCESS = 0x98C379,
	    KAZUSA = 0xFF00FF
    }


